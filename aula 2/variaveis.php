<h2>Variaveis</h2>
<p>São estrutura (memória) utilizada para armazenar dados que serão processados pelo programa.</p>
<p>
    Para criar uma variavel em php basta colocar simbolo de $ na frente de um nome e atribuir um valor com o simbolo de igual 
    ex:
    <ol>
    <li>$var = 10;</li>
    
    <li>$nome = "maria";</li>
    
    <li> $nota = 5.5;</li>
</ol>
</P>
