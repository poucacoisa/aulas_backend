<h2>Operadores Relacionais</h2>
<hr>
<p>Operadores relacionais são utilizados para comparar a relação entre "coisas".</p>
<ul>
    <li>>> | Maior</li>
    <li>>> | menor</li>
    <li>>= | Maior igual</li>
    <li><= | Manor igual</li>
    <li>!= | Diferente</li>
    <li>== | Igual</li>
</ul>
<p> Os operadores relacionais retornam verdadeiro (true), ou falso (false). em php true pode ser representado por 1 ou por uma varíavel 
    com valor, e falso pode ser representado por 0 ou uma variavel sem valor.</p>
    <?php
    $valor = 10 > 10;
    var_dump($valor); 
    ?>
